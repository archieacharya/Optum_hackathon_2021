package com.nirogo.www;

public class PostList {


    private String head;
    private String desc;
    private String img;


    PostList(String head, String desc, String img) {
        this.head = head;
        this.desc = desc;
        this.img = img;
    }






    public String getHead() {
        return head;
    }


    public String getDesc() {
        return desc;
    }

    public String getImg() {
        return img;
    }


}
